 <h3>HISTORY</h3>
<p> Delhi Public School,Patna is one of the most prestigious schools in India. Founded as a Public School in 2005 in Patna,
 it is a private institution run by the Delhi Public School Society.
  Delhi Public School,Patna is affiliated to the Central Board of Secondary Education (CBSE), which is the largest educational board in the country. It is recognized by the Department of Education, Govt. of NCT Delhi and the Ministry of HRD, Govt. of India. Over 5000 schools in India, with over 80,000 students, are members of the Board. 
The school is also affiliated to the Indian Public Schools' Conference (IPSC), and the National Progressive Schools' Conference (NPSC). The members of these organisations include some of the premier schools in the country.
Life at DPSRKP centres on a shared commitment to academic excellence, intellectual growth, art, athletics, high standards of ethical awareness, sportsmanship, and community service. The school's traditions and accessibility to a broad curriculum add depth to each student&rsquo;s life.<br>
The school upholds the founders' commitment to excellence in all fields, with emphasis on its motto Service Before Self.<br/>
 </p>
 <h3>SCHOOL PROFILE</h3>
<p> DPS is a co-educational day-cum-boarding school, with approximately 9,500 students on its rolls. These children, in the Junior and Senior branches, study in the three different campuses at East of Kailash, Vasant Vihar and R.K.Puram.
  The school is among the most distinguished members of the Delhi Public School Society. It is a path breaker in the pursuit of excellence. Its endeavour of integrating quality with quantity is reflected in the pivotal role it has played in the setting up of DPS Vasant Kunj, DPS Faridabad and DPS Manali at the national level. It has also promoted three schools abroad in Kuwait, Nepal and Indonesia. As their Linking School it also co-ordinates their activities. 
 The school has also extended its expertise further and in collaboration with the Government of Haryana, has taken up 3 Schools in the under-privileged area of Mewat, to augment and enhance their standards and make them more conducive to teaming.
 The school considers education to be a life-long process which should have a strong foundation. The goal of the school is to inculcate in the students a love for learning and a desire to excel at every level. The school also aims at equipping the students with the intellectual and practical skills that are necessary to meet the challenges in the future.</p>
 To sum up, the mission of Delhi Public School,Patna is &quot;to open doors and open minds&quot; and prepare the ground for the future of the nation.
</p>
 <h3 >CULTURE</h3>
 <p > In the portals of Delhi Public School,Patna students discover their own talents, and get an opportunity to develop them to the fullest. The school provides an invigorating and competitive atmosphere, created by excellent facilities and guidance provided by a highly qualified and dedicated faculty.
 The values, which are ingrained help to promote confidence, direction, and critical thinking skills, leading to the development of well-adjusted, adaptable and integrated personalities. In other words, DPS R.K. Puram offers comprehensive and holistic education.
 Besides being committed to academic excellence and providing education for all round d evelopment, another special characteristic of DPS R.K. Puram is the appreciation of the worth of the each student. The School is equally committed to the under-represented and less-privileged segments of the population, such as gifted applicants whose parents could not attend school, and children with high potential facing difficult financial circumstances.
 A major landmark development has been the inclusion of the physically and mentally handicapped children into the mainstream of school life. This  contributes to a strong sense of community life, so characteristic of the school. In other words, children belonging to every strata of society are given the opportunity to study here. The school, does not in any way, discriminate on the basis of race, colour, religion, sex, caste or creed, in the implementation of its admission policy.</p>
 These qualities have placed DPS Patna on the forefront. There has been a continuity of purpose, underlying the change and growth of the School. Over the years, DPS R.K. Puram has steadily reflected a spirit of innovation in response to need, and has broadened its educational mission, by creating an academic environment that fosters close association and the exchange of ideas with some of the top schools in the nation and the world.
 Its membership with the IPSC (Indian Public School's Conference) has brought it into regular interaction with schools of national standing such as Mayo College, Ajmer; Scindia School, Gwalior; The Doon School, Dehradun; Bishop Cotton, Simla Hills; St. Xavier's and La-Martiniere at Calcutta; which has further inculcated a healthy spirit of competition and strong bonds of brotherhood, conducive to national integration.                            
  All the academic programs and activities at DPS R.K. Puram work towards one purpose - to help students develop lives of significance for themselves and for others, true to the traditions of the School Motto &quot;Service Before Self&quot;.
</p>
