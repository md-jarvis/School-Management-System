The DPS Society is a non-profit, non-proprietary,
 private, educational organization. This Global
 Network of over 200 English medium, co-educational, 
secular schools provides education from Pre-Nursery/Nursery to Class XII. 
The DPS Family – with its transcontinental identity, is not merely a list
 of institutions, persons or facts;
 it is a network of values, systems and relationships.
For more than six decades ,the DPS Society has developed with a spirit and a vision.
 Today it stands as a citadel of learning and a harbinger of quality consciousness, committed to developing the minds and initiatives of children. The family is growing every day. The core schools have been set up by the Society and schools under aegis of the DPS Society have been established globally in collaboration with concerned public, private sector undertakings and individual entrepreneurs. Believing that quality education is the high road to both individual and national progress,
 the Society has proved to be a significant trend setter in the field of school education.
DPS GN one of the Core Schools under the control and support of the  DPS Society New Delhi,  having commenced its operations effectively in 1998, has certainly come of an age, with a strength 3900 students (approx.) and 205 dedicated faculty members now, from classes Nursery to 12th.  Situated in a very systematically planned township forming the suburb of Delhi, it has been imparting a holistic education to its lively students under the guidance of dynamic principals, since its inception, who have acted as the true mentors.
<br />
<br />

The majestic, modern and ever expanding building blocks, with an aesthetically designed Junior School Block and the grand layout of the school, which sprawls over 15 acres of land in lush green surroundings, a great atmosphere for growth and joyful learning is ensured.
 Lauded for its impressive Board results and academic excellence in the entire region for last one decade, the school, has opened its doors to diverse forms of learning to secure a sound education for its students. State-of- the- art infrastructural facilities include an impressive multi-purpose auditorium, well-equipped Science Labs, Resource Rooms,  Social Science, Computer and Maths Labs which provide opportunities for ‘Learning by Doing’ enabling the students to keep abreast of the challenges of the modern technological age and global needs. The School Libraries are consciously perceived & built as a growing asset to inculcate the reading habit in children across all ages.
The academic excellence is well balanced along with the pursuit of sports activities like Swimming, Tae-kwondo, 400 m. Athletic Track, Horse Riding, Yoga, impressive Synthetic Tennis Courts etc.


Several exciting participation opportunities are offered through initiatives like Mind Vision, DPS MUN, well–planned International Exchange Programmes and other annual Inter-DPS sports competitions and cultural festivals, together, leading to an experiential learning to address diverse issues that confront our largely inter-connected world.
The spirit of social responsibility is amply expressed by the adoption of 25 girl children from disadvantaged background for their education and general enhancement of their personalities, and also by imparting free education and other facilities to more than 200 children form economically weaker sections of the society. The School Alumni has indeed done their Alma Mater proud by exploring great opportunities in the country and abroad to give back to society what they were nurtured for.
Our students and the teachers who take to professional orientations and trainings as their privileged right, realize that they have a stake in the kind of educational institution DPS Greater Noida is today and will be in the years to come, and this realization defines our collective aspirations & future goals.
