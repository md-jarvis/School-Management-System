<div id="pid2">Ultra Modern SMART CLASSROOM Technology - The DPS Pinjore SMART CLASSROOM technology is a world-class technology enabled teaching tool in special classrooms are equipped with multiple display monitors and big screen plasma TVs and a PC for the teacher, which are digitally connected to the knowledge centre. Through the knowledge centre excellent teaching resources such as animation clips, videos, pictures, diagrams and working models etc. are brought straight in the classroom.

Language Development Programmes through the Language Lab
The Language Development Programme supports students whose learning styles have created barriers to academic progress and also to remove accents and any hesitation that the child may have in speaking a language. This programme is designed to help them develop awareness of how they learn, improve their language skills and help them internalise the study routines that facilitate the learning process. We offer English, Hindi, Sanskrit, Punjabi and French as taught languages. If the child wishes to opt for any other language, the child may be permitted to do it under self study with the Principal’s authorization.

Science Park
The DPS Pinjore Science Park, the first in Haryana, is an innovative and novel concept of teaching science to the students through the ‘learning through doing’ method. The open-air play models and exhibits explain various formulas and principles of Physics and General Science. Live models related to Newton's Disc Persistence of Vision, Musical Pipes, Lever Arm, Transfer of energy etc. have been installed on the lush green lawns of the DPS campus. This is an endeavour to help our children develop a scientific attitude and to understand the application of basic concepts of Science in their daily lives. The interest that the Science Park generates is also reflected in the children’s attitude towards other subjects at the school.

