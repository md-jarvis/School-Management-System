<div id="pid3">Well Stocked Library
Self study and reading are important skills towards developing the habit of life-long learning which is an indispensable tool
 for future careers. For this purpose a well equipped and stocked library with different types of books, magazines, periodicals, and newspapers
  is available. <br/><br/>Every child is inspired and encouraged to go to the library during library periods as well as in free 
  time. A qualified librarian is appointed to maintain the library which has been especially designed with a beautiful
   view of the Shivaliks.
<br /><br />
Transportation for Day Scholars
School transport is available for Panchkula, Kalka, Parwanoo, Baddi, Surajpur, Zirakhpur, and other neighbouring areas. 
The transportation charges depend on the distance of the school from the branch.
The total number of books in the library is 15,000 with the number increasing with each passing day.<br /><br />
 The library has sections ranging from textbooks and reference books to fiction and general knowledge.
  There is a vast array of books and magazines available on general topics like travel, astrology, philosophy etc.
We have a well stocked library to support the curriculum, develop life-long learning abilities and foster a love of reading.
Library resources comprise of books, magazines and newspapers. Children metamorphose into thoughtful confident and
 productive individuals by the judicious use of library.
<br /><br />
Reading rooms- (2 in number) Believing in the beautiful thought that Every man who knows how to read has it in his power to magnify himself, to multiply the ways in which he exists, to make his life full, significant and interesting., We have created 2 reading rooms in primary wing.

Kids zone- To tap into the depths of young minds in the right way and to nurture these young minds to their fullest potential.

ECE

The ECE  has a well stocked spacious library of around 850-900 books, which provides great exposure to the children through its visually rich , children’s classics, folk tales, video CDs. The children enjoy sitting in the midst of the knowledge zone where they learn and grasp in a fun way.

