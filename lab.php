<h2>COMPUTER LAB</h2>
<p >
<br />
DPS, Durg has three well-furnished Computer labs,
 one in Primary wing for classes-I to V and two labs in Senior Wing for classes-VI to XII.
 Each laboratory is fully equipped with 20 networked PCs under LAN with Internet connection. 
Students enjoy interactive learning process as well as demonstration of educational CD’s
 through PCs and LCD projector in the laboratory. Special care is taken regularly to 
guide the students for national level competitive examinations in computers like National Cyber Olympiad(NCO), 
International Informatics Olympiad(IIO), National Graphics Championship(NGC) and many other.<br />
</p>
